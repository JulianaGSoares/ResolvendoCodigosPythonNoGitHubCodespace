# Vamos solicitar como entrada dois números e depois vamos realizar uma operação simples entre eles.

num1 = int(input("Insira um numero: "))
num2 = int(input("Insira outro numero: "))

adicao = num1 + num2
subtracao = num1 - num2
divisao = num1 / num2
multiplicacao = num1 * num2

print("Resultado adicao: ", adicao)
print("Resultado subtracao: ", subtracao)
print("Resultado divisao: ", divisao)
print("Resultado multiplicacao: ", multiplicacao)
